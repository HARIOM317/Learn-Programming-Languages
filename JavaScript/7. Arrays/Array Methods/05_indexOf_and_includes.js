let fruits = ["Apple", "Banana", "Cherry", "Mango"];

// 1. indexOf() - Returns the first index at which a given element can be found, or -1 if not present.
console.log(fruits.indexOf("Banana")); // Output: 1
console.log(fruits.indexOf("Papaya")); // Output: -1

// __________________________________________________

// 2. includes() - Checks if an array contains a specified element, returning true or false.
console.log(fruits.includes("Mango"));
console.log(fruits.includes("Orange"));
