let fruits = ["Apple", "Orange", "Banana"];
console.log(fruits);

// unshift() - to insert new item at the start of array
fruits.unshift("Mango");
fruits.unshift("Papaya");
fruits.unshift("Grapes");

console.log(fruits);

// shift() - to remove first item of array
fruits.shift();
fruits.shift();

console.log(fruits);
