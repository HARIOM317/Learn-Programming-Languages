// Set - A Set in JavaScript is used to store a unique collection of items, meaning no duplicates are allowed.

// 1. Using an array
let s1 = new Set([10, 20, 10, 30, 30, 40, 50, 40]);
console.log("Set s1 :", s1);
console.log("Type of s1 :", typeof s1);

let s2 = new Set(["Apple", "Banana", "Papaya", "Apple", "Mango", "Banana"]);
console.log("Set s2 :", s2);

// 2. Using string
let s3 = new Set("happy lucky cooking fooooood");
console.log("Set s3 :", s3);

// 3. An empty set
let s4 = new Set();
