// Using a string primitive
let primitiveStr = "Hello, World!";
console.log(primitiveStr); // Output: "Hello, World!"
console.log(primitiveStr.toString()); // Output: "Hello, World!"

// Using a String object
let objectStr = new String("Hello, World!");
console.log(objectStr);
console.log(objectStr.toString()); // Output: "Hello, World!"

// Using a number
let num = 10005;
console.log(num, typeof num);
console.log(num.toString(), typeof num.toString());
