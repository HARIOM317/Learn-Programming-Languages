let str = "       Hello, World!     ";

// trim()
let trimmedStr = str.trim();
console.log(trimmedStr, " Length :", trimmedStr.length);

// trimStart()
let trimmedStart = str.trimStart();
console.log(trimmedStart, " Length :", trimmedStart.length);

// trimEnd()
let trimmedEnd = str.trimEnd();
console.log(trimmedEnd, " Length :", trimmedEnd.length);
