// Assignment Operators
console.log("Assignment Operators");
let x = 5;

x += 10; // x = 5 + 10 = 15
x -= 5; // x = 15 - 5 = 10
console.log("x = ", x);

x *= 10; // x = 10 * 10 = 100
x /= 5; // x = 100 / 5 = 20
console.log("x = ", x);

x **= 2; // x = 20 ^ 2 = 400
x %= 5; // x = 400 % 5 = 0
console.log("x = ", x);
