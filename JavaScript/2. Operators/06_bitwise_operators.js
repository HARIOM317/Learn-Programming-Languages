let a = 10;
let b = 20;

console.log("a = ", a);
console.log("b = ", b);

// Bitwise Operator
console.log("\nBitwise Operators");
console.log("a & b = ", a & b);
console.log("a | b = ", a | b);
console.log("a ^ b = ", a ^ b);
console.log("~a = ", ~a);
console.log("~b = ", ~b);
console.log("a << 1 = ", a << 1);
console.log("a >> 1 = ", a >> 1);
console.log("a >>> 1 = ", a >>> 1); // It is just like the >> operator, except that the bits shifted in on the left are always zero.
