SELECT CURRENT_DATE();
SELECT CURRENT_TIME();
SELECT NOW();  -- full timestamp

-- Extract only date part
SELECT DATE(NOW());

SELECT YEAR('2024-05-21');
SELECT MONTH('2024-05-21');
SELECT DAY('2024-05-21');

-- Days between two dates
SELECT DATEDIFF('2025-01-10', '2025-01-01');

-- ADD DATE
SELECT DATE_ADD('2025-01-01', INTERVAL 10 DAY);

-- SUBTRACT DATE
SELECT DATE_SUB('2025-01-01', INTERVAL 1 MONTH);

-- Day or Month Name
SELECT DAYNAME('2025-01-01');  -- Wednesday
SELECT MONTHNAME('2025-01-01'); -- January
